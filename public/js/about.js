// Dynamic counter animation (if needed)
function animateCounter(element, target, duration = 2000) {
  let start = 0;
  const increment = target / (duration / 16);

  const timer = setInterval(() => {
    start += increment;
    element.textContent = Math.floor(start);

    if (start >= target) {
      element.textContent = target;
      clearInterval(timer);
    }
  }, 16);
}

function animateHeading(card) {
  const heading = card.querySelector(".story-heading");
  gsap.fromTo(
    heading,
    { y: 0 },
    {
      y: -10,
      duration: 0.2,
      yoyo: true,
      repeat: 1,
      ease: "power1.inOut",
    }
  );
}

// Add hover effects to cards
document.querySelectorAll(".leader-card, .value-card").forEach((card) => {
  card.addEventListener("mouseenter", function () {
    this.style.transform = "translateY(-10px) scale(1.02)";
  });

  card.addEventListener("mouseleave", function () {
    this.style.transform = "translateY(0) scale(1)";
  });
});

// Loading animation
window.addEventListener("load", () => {
  document.body.style.opacity = "1";
});

// Smooth reveal animation on scroll
const revealElements = document.querySelectorAll(
  ".leader-card, .value-card, .story-text, .story-visual"
);

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry, index) => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          entry.target.style.opacity = "1";
          entry.target.style.transform = "translateY(0)";
        }, index * 100);
      }
    });
  },
  {
    threshold: 0.1,
  }
);

revealElements.forEach((el) => {
  el.style.opacity = "0";
  el.style.transform = "translateY(30px)";
  el.style.transition = "all 0.6s ease";
  revealObserver.observe(el);
});
